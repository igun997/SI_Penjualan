    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) listPelanggan.getModel();
            int selectedRowIndex = listPelanggan.getSelectedRow();
            String id = model.getValueAt(selectedRowIndex, 1).toString();
            String[] data = {id};
            pelanggan pl = new pelanggan();
            pl.setKode(id);
            int status = plServe.delete(pl);
            if(status > 0)
            {
                JOptionPane.showMessageDialog(this,"Hapus Berhasil");
            }else{
                JOptionPane.showMessageDialog(this,"Hapus Gagal");
            }
            setJTable();
            btnDel.setEnabled(false);
        } catch (SQLException ex) {
            Logger.getLogger(v_pelanggan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDelActionPerformed

