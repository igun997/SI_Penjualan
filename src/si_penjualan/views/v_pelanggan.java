    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        enable_field();
        statusInput = 1;
        btnBatal.setEnabled(true);
        DefaultTableModel model = (DefaultTableModel) listPelanggan.getModel();
        int selectedRowIndex = listPelanggan.getSelectedRow();
       
       txtNopel.setText(model.getValueAt(selectedRowIndex, 1).toString());
       txtNama.setText(model.getValueAt(selectedRowIndex, 2).toString());
       if(model.getValueAt(selectedRowIndex, 3).toString() == "Perempuan")
       {
           cmbJK.setSelectedIndex(2);
       }else{
           cmbJK.setSelectedIndex(1);
       }
       txtAlamat.setText(model.getValueAt(selectedRowIndex, 4).toString());
       txtNoTelp.setText(model.getValueAt(selectedRowIndex, 5).toString());
       btnSimpan.setEnabled(true);
    }//GEN-LAST:event_btnEditActionPerformed

