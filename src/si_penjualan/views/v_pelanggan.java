      public void updateRecord()
      {
        String nopel,nama,jk,alamat,notlp;
        nopel = txtNopel.getText();
        nama = txtNama.getText();
        alamat = txtAlamat.getText();
        notlp = txtNoTelp.getText();
        jk = cmbJK.getSelectedItem().toString();
        float notlpon = Float.parseFloat(notlp);
        if(jk != "= Pilih =" && alamat != "" && notlpon > 0 && Integer.parseInt(nopel) > 0)
        {
            pelanggan pl = new pelanggan();
            pl.setKode(nopel);
            pl.setNama(nama);
            pl.setJK(jk);
            pl.setAlamat(alamat);
            pl.setTlp(notlp);
            int status;
            try {
                status = plServe.update(pl);
                if(status > 0)
                {
                    JOptionPane.showMessageDialog(this,"Update Pelanggan Berhasil");
                }else{
                    JOptionPane.showMessageDialog(this,"Update Pelanggan Gagal");
                }
                setJTable();
            } catch (SQLException ex) {
                Logger.getLogger(v_pelanggan.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }else{
            JOptionPane.showMessageDialog(this,"Isilah Semua Field");
        }
        cleanText();
        btnSimpan.setEnabled(false);
      }
